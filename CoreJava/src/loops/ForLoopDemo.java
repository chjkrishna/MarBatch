package loops;

public class ForLoopDemo {

    public static void main(String[] args) {
        // for (initialisation; evaluation; incrementation) {...} // 1, 2, 3 ,4....10
        for (int i =1; i <=10; i = i +2) { // 2,4,6,...10
            System.out.println(i);
            //System.out.println("Jay");
        }


    }
}
